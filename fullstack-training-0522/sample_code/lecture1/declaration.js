// Using var keyword
var name = "Aaron Zhang";
console.log(name); // Outputs:Aaron Zhang 

// Re-assigning value to variable declared with var
name = "Alex Chen";
console.log(name); // Outputs:Alex Chen 

// Using let keyword
let age = 25;
console.log(age); // Outputs: 25

// Re-assigning value to variable declared with let
age = 26;
console.log(age); // Outputs: 26

// Using const keyword
const pi = 3.14;
console.log(pi); // Outputs: 3.14

